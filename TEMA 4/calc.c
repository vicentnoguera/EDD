#include <stdio.h>

int suma(int a, int b)
{
    return a + b;
}

int resta(int a, int b)
{
    return a - b;
}

int multiplicacio(int a, int b)
{
    return a * b;
}

int divisio(int a , int b)
{
    return a / b;
}

int major(int a , int b)
{
    if (a>b)
    {
    return a;
    }
    else{
        return b;
    }
    
}